    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (txtNome.getText().isEmpty() || !txtCpf.isEditValid() || !txtTelefone.isEditValid()) {
            InterfaceUtils.exibeAlerta(this, "Erro ao editar Cliente", "Um ou mais campos obrigat√≥rios est√£o em branco. Por favor preencha-os e tente novamente.");
        } else {
            try {
                cliente.setNome(txtNome.getText());
                cliente.setRg(txtRg.getText());
                cliente.setTelefone(txtTelefone.getText());
                cliente.setDataNasc(txtDataNascimento.getText());
                cliente.setEndereco(txtEndereco.getText());
                cliente.setEmail(txtEmail.getText());
                cliente.alterar();
                InterfaceUtils.exibeMensagem(this, "Cliente alterado", "Cliente editado com sucesso!");
                dispose();
            } catch (Exception ex) {
                InterfaceUtils.exibeAlerta(this, "Erro ao salvar", "Houve um erro ao salvar o Cliente. Por favor tente novamente.");
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

