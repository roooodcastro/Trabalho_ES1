    private void setCliente(Cliente cliente) {
        this.cliente = cliente;
        if (cliente != null) {
            txtNomeCliente.setText(cliente.getNome());
        }
        else {
            txtNomeCliente.setText("");
        }
    }
    
    private void setAula(Aula aula) {
        this.aula = aula;
        if (aula != null) {
            txtNomeAula.setText(aula.getNome());
        }
        else {
            txtNomeAula.setText("");
        }
    }
