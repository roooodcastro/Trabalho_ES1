    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if (txtNomeCliente.getText().isEmpty() || txtNomeAula.getText().isEmpty()) {
            InterfaceUtils.exibeAlerta(this, "Erro ao incluir cliente", "Por favor escolha um cliente e uma aula");
        } else {
            try {
                aula.addAluno(cliente);
                aula.alterar();
                InterfaceUtils.exibeMensagem(this, "Sucesso", "O cliente selecionado foi inclu√≠do na aula!");
                dispose();
            } catch (Exception ex) {
                InterfaceUtils.exibeAlerta(this, "Erro ao salvar", "Houve um erro ao salvar a aula. Por favor tente novamente.");
            }
        }
    }//GEN-LAST:event_btnIncluirActionPerformed

